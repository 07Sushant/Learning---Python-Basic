#Determining either a student is eligible to give the exam or not?
Lecture = int(input("total number of lecture in your course: "))
Attended = int(input("Number of lecture attended: "))
percentage = ((Attended)/(Lecture))*100
if percentage>=75:
    print("You are eligible")
else :
    print("Not eligible")
